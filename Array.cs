// 1.) DETERMINE WHAT TYPE OF DATA THE ARRAY CONTAINS
// 2.) APPEND SQUARE BRACKETS ([]) TO THE END OF THE DATA TYPE
// 3.) INCLUDE A VARIABLE NAME
// 4,) ADD DATA TO THE ARRAY WITHIN CURLEY BRACKETS ({})

---------------------   STRING ARRAY   -----------------------------
string[] fruits = { "apples", "bananas", "oranges", "grapes" };

fruits;
// {"apples", "bananas", "oranges", "grapes"}

---------------------  ALTERING ARRAY   -----------------------------

fruits[1];
// "bananas"

fruits[3];
// "grapes"

string favoriteFruit = fruits[0];

favoriteFruit;
// "apples"
---------------------   INT ARRAY   -----------------------------
int[] primeNumbers = { 2, 3, 5, 7, 11 };

primeNumbers;
// {2, 3, 5, 7, 11}




